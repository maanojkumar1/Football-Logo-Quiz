package com.example.ryanh.footballlogo;

public class Logo {

    String name;
    int img;



    public Logo (String name, int img){    // to store object in the class

        this.name=name;
        this.img=img;


    }
    public String getName() {
        return name;
    }

    public int getImg() {
        return img;
    }

}
